// Helper function để lấy recipes theo category
export const getRecipesByCategory = (category, limit = 8) => {
  return mockRecipes
    .filter(
      (recipe) => recipe.category === category || recipe.mealType === category
    )
    .slice(0, limit);
};

// Helper function để lấy random recipes
export const getRandomRecipes = (limit = 8) => {
  const shuffled = [...mockRecipes].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, limit);
};

// Helper function để lấy items theo category type, loại bỏ item hiện tại
export const getCategoryItems = (categoryType, currentItem) => {
  const categoryData = mockCategoryItems[categoryType];
  if (!categoryData || !categoryData.items) {
    return [];
  }
  
  return categoryData.items.filter((item) => item.slug !== currentItem);
};